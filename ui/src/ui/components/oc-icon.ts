export type OcIconName = keyof typeof ICON_PATHS;

/** All registered icon names. */
export const OC_ICON_NAMES = Object.keys(ICON_PATHS) as OcIconName[];

/**
 * <oc-icon> — Unified icon component.
 *
 * @property {OcIconName} name - Icon identifier.
 * @property {"xs"|"sm"|"md"|"lg"} size - Predefined size.
 * @property {"light"|"regular"|"bold"} weight - Stroke weight.
 *
 * @example
 *   <oc-icon name="agents" size="md"></oc-icon>
 *   <oc-icon name="refresh" size="sm" weight="light"></oc-icon>
 */
@customElement("oc-icon")
export class OcIcon extends LitElement {
  @property({ type: String }) name: OcIconName = "default";
  @property({ type: String }) size: "xs" | "sm" | "md" | "lg" = "md";
  @property({ type: String }) weight: "light" | "regular" | "bold" = "regular";
  @property({ type: String, attribute: "aria-label" }) declare ariaLabel: string | null;

  static styles = css`
    :host {
      display: inline-flex;
      align-items: center;
      justify-content: center;
      vertical-align: middle;
      flex-shrink: 0;
    }
    svg {
      display: block;
      fill: none;
      stroke: currentColor;
      stroke-linecap: round;
      stroke-linejoin: round;
    }
    :host([size="xs"]) svg { width: 14px; height: 14px; }
    :host([size="sm"]) svg { width: 16px; height: 16px; }
    :host([size="md"]) svg { width: 20px; height: 20px; }
    :host([size="lg"]) svg { width: 24px; height: 24px; }
    :host([weight="light"]) svg { stroke-width: 1.5; }
    :host([weight="regular"]) svg { stroke-width: 2; }
    :host([weight="bold"]) svg { stroke-width: 2.5; }
  `;

  render() {
    const pathFn = ICON_PATHS[this.name];
    if (!pathFn) {
      return nothing;
    }
    const isDecorative = !this.ariaLabel;
    return html`
      <svg
        viewBox="0 0 24 24"
        role=${isDecorative ? "presentation" : "img"}
        aria-hidden=${isDecorative ? "true" : "false"}
        aria-label=${this.ariaLabel ?? nothing}
      >${pathFn()}</svg>
    `;
  }
}

